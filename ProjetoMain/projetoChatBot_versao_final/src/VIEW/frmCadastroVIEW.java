    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome, email, senha,dt_nasc, telefone;
        
        
        nome = txtNome.getText();
        email = txtEmail.getText();
        senha = txtSenha.getText();
        dt_nasc = txtDataNascimento.getText();
        telefone = txtTelefone.getText();
        
        CadastroDTO objcadastrodto = new CadastroDTO();
        objcadastrodto.setNome(nome);
        objcadastrodto.setEmail(email);
        objcadastrodto.setSenha(senha);
        objcadastrodto.setDt_nasc(dt_nasc);
        objcadastrodto.setTelefone(telefone);
        
        CadastroDAO objcadastrodao = new CadastroDAO();
        objcadastrodao.cadastrarUsuario(objcadastrodto);
        
        JOptionPane.showMessageDialog(null, "Cadastro Realizado");
        dispose();
        new frmLoginVIEW().setVisible(true);
        
        
      
        
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

